#include <stdio.h>
int main()
{
    int a = 10;
    int b = 5;
    int sum = a + b;
    printf("Sum = %d\n", sum);
    int sub = a - b;
    printf("Subtraction = %d\n", sub);
    int prod = a * b;
    printf("Product = %d\n", prod);
    int div = a / b;
    printf("Division = %d\n", div);
    return 0;
}